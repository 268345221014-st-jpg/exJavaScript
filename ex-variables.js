// การประกาศตัวแปร (Declaration) และการกําหนดค่า (Assignment)
let playerName = "สมชาย";
let score = 0;
// การกำ หนดค่าใหม่ (Re-assignment) ให้ตัวแปร let
score = 10;
console.log(playerName); // ผลลัพธ์: สมชาย
console.log(score); // ผลลัพธ์: 10
// การใช้ const สําหรับค่าคงทีc
const pi = 3.14159;
